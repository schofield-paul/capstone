import React, { useEffect, useState } from 'react';
import { useDispatch, useSelector } from 'react-redux';
import { useNavigate, useParams } from 'react-router-dom';
import {
  selectSingleEvent,
  fetchSingleEvent,
  editEventAsync,
} from '../../slices/eventsSlice';

const EditEvent = () => {
  const dispatch = useDispatch();
  const { id } = useParams();
  const event = useSelector(selectSingleEvent);
  const navigate = useNavigate();

  useEffect(() => {
    dispatch(fetchSingleEvent(id));
  }, [dispatch, id]);

  const [formData, setFormData] = useState({
    eventStart: event.event_start,
    eventEnd: event.event_end,
    topic: event.topic,
    description: event.description,
    zipCode: event.zip_code,
  });

  const handleEdit = async (e) => {
    e.preventDefault();
    await dispatch(editEventAsync({ id, formData }));
    navigate(-1);
  };

  return (
    <>
      <div className="w-100 h-vh100 bg-pale-blue">
        <form onSubmit={handleEdit}>
          <div className="grid gap-6 mb-6 md:grid-cols-2">
            <div>
              <label
                htmlFor="event_start"
                className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
              >
                Event Start:
              </label>
              <input
                type="datetime-local"
                id="event_start"
                value={formData.eventStart}
                onChange={(evt) => {
                  setFormData({
                    ...formData,
                    eventStart: evt.target.value,
                  });
                }}
                className="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                // required
              ></input>
            </div>
            <div>
              <label
                htmlFor="event_end"
                className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
              >
                Event End:
              </label>
              <input
                type="datetime-local"
                id="event_end"
                value={formData.eventEnd}
                onChange={(evt) => {
                  setFormData({
                    ...formData,
                    eventEnd: evt.target.value,
                  });
                }}
                className="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                // required
              ></input>
            </div>

            <div>
              <label
                htmlFor="topic"
                className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
              >
                Type of Event:
              </label>
              <input
                type="text"
                id="topic"
                value={formData.topic}
                onChange={(evt) => {
                  setFormData({
                    ...formData,
                    topic: evt.target.value,
                  });
                }}
                className="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                placeholder="Birthday, Group Playdate, etc..."
                // required
              ></input>
            </div>
            <div>
              <label
                htmlFor="zip_code"
                className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
              >
                Location (Zip Code) :
              </label>
              <input
                type="number"
                id="zip_code"
                value={formData.zipCode}
                onChange={(evt) => {
                  setFormData({
                    ...formData,
                    zipCode: evt.target.value,
                  });
                }}
                className="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
                placeholder="01010"
                // required
              ></input>
            </div>
          </div>
          <div className="mb-6">
            <label
              htmlFor="description"
              className="block mb-2 text-sm font-medium text-gray-900 dark:text-white"
            >
              Event Details:
            </label>
            <input
              type="text"
              id="description"
              value={formData.description}
              onChange={(evt) => {
                setFormData({
                  ...formData,
                  description: evt.target.value,
                });
              }}
              className="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5 dark:bg-gray-700 dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 dark:focus:border-blue-500"
              placeholder="Tell us a little bit more about this event"
              //   required
            ></input>
          </div>

          <div className="flex items-start mb-6">
            <div className="flex items-center h-5">
              <input
                id="remember"
                type="checkbox"
                // value=""
                className="w-4 h-4 border border-gray-300 rounded bg-gray-50 focus:ring-3 focus:ring-blue-300 dark:bg-gray-700 dark:border-gray-600 dark:focus:ring-blue-600 dark:ring-offset-gray-800"
                // required
              ></input>
            </div>
            <label
              htmlFor="remember"
              className="ml-2 text-sm font-medium text-gray-900 dark:text-gray-300"
            >
              I agree with the{' '}
              <a
                href="/"
                className="text-blue-600 hover:underline dark:text-blue-500"
              >
                terms and conditions
              </a>
              .
            </label>
          </div>
          <button
            type="submit"
            className="text-white bg-blue-700 hover:bg-blue-800 focus:ring-4 focus:outline-none focus:ring-blue-300 font-medium rounded-lg text-sm w-full sm:w-auto px-5 py-2.5 text-center dark:bg-blue-600 dark:hover:bg-blue-700 dark:focus:ring-blue-800"
          >
            Submit
          </button>
        </form>
      </div>
    </>
  );
};

export default EditEvent;
